public class Categoria {
        int Id;
        String NomeProduto;
        String Descricao;
        String Setor;

        public Categoria (int id, String NomeProduto, String Descricao, String Setor) {
            this.Id = Id;
            this.NomeProduto = NomeProduto;
            this.Descricao = Descricao;
            this.Setor = Setor;
        }
}
